# Esta función recibe dos números como parámetros y nos devuelve la suma.
def sumar(numero1, numero2):
    resultado = numero1 + numero2
    return resultado

# Llamamos a la función y guardamos el resultado que nos "devuelve" en una variable.
suma_total = sumar(8, 3)

print("El resultado de la suma es:", suma_total)


# Esta función recibe dos números como parámetros y nos devuelve la resta.
def resta(numero1, numero2):
    resultado = numero1 - numero2
    return resultado

# Llamamos a la función y guardamos el resultado que nos "devuelve" en una variable.
resta_total = resta(10, 3)

print("El resultado de la resta es:", resta_total)